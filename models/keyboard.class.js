/**
 * Represents the current keyboard input state for game controls.
 */
class Keyboard {
  /** @type {boolean} */
  LEFT = false;

  /** @type {boolean} */
  RIGHT = false;

  /** @type {boolean} */
  UP = false;

  /** @type {boolean} */
  D = false;

  /** @type {boolean} */
  SPACE = false;

  /** @type {boolean} */
  ATTACK = false;

  /**
   * Creates a keyboard input state container.
   * Event listeners are registered externally.
   */
  constructor() {}
}
